/**
 *  * Export JS function "raiseValueToPower2" to raise one number to power of two
 
* - raise base to 2
 
 * - define signature
  * - input: one number
  * - output: one number
*/

/**
 * Raise one number to power of two
 * @param {number} base
 * @returns number^2
 */
const raiseValueToPower2 = function (base) {
  return Math.pow(base, 2);
};

export default raiseValueToPower2;
